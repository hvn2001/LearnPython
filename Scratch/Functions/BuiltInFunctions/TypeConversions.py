print("---int()----")
print(int("12") * 10)  # String to integer
print(int(20.5))  # Float to integer
print(int(False))  # Bool to integer

# print (int("Hello")) # This wouldn't work!

print("---ord()----")
print(ord('a'))
print(ord('0'))

print("---float()----")
print(float(24))
print(float('24.5'))
print(float(True))

print("---str()----")
print(str(12) + '.345')
print(str(False))
print(str(12.345) + ' is a string')

print("---bool()----")
print(bool(10))
print(bool(0.0))
print(bool("Hello"))
